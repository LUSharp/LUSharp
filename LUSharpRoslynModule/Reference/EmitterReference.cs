namespace LUSharpRoslynModule.Reference;

/// <summary>
/// Produces expected SimpleEmitter Luau output for validation.
/// We manually construct the expected output since RoslynSource files
/// are excluded from compilation. Uses same C# inputs as WalkerReference.
/// </summary>
public static class EmitterReference
{
    public static void PrintAll()
    {
        // Test input: same Calculator class + enum from WalkerReference
        string input = @"class Calculator {
    int _result;

    Calculator(int initial) {
        _result = initial;
    }

    int Add(int a, int b) {
        return a + b;
    }

    void Reset() {
        _result = 0;
    }

    static int Max(int a, int b) {
        if (a > b) {
            return a;
        }
        return b;
    }
}

enum Operation { Add, Subtract = 1, Multiply }";

        Console.WriteLine("=== Emitter Test ===");
        Console.WriteLine("Input:");
        Console.WriteLine(input);
        Console.WriteLine();

        // Expected Luau output:
        Console.WriteLine("=== Expected Luau Output ===");
        Console.WriteLine("--!strict");
        Console.WriteLine("-- Auto-generated by SimpleEmitter");
        Console.WriteLine("");
        Console.WriteLine("type Calculator_self = {");
        Console.WriteLine("\t_result: number;");
        Console.WriteLine("}");
        Console.WriteLine("");
        Console.WriteLine("local Calculator = {}");
        Console.WriteLine("Calculator.__index = Calculator");
        Console.WriteLine("export type Calculator = typeof(setmetatable({} :: Calculator_self, Calculator))");
        Console.WriteLine("");
        Console.WriteLine("function Calculator.new(initial: number): Calculator");
        Console.WriteLine("\tlocal self = setmetatable({");
        Console.WriteLine("\t\t_result = 0,");
        Console.WriteLine("\t} :: Calculator_self, Calculator)");
        Console.WriteLine("\tself._result = initial");
        Console.WriteLine("\treturn self");
        Console.WriteLine("end");
        Console.WriteLine("");
        Console.WriteLine("function Calculator.Add(self: Calculator, a: number, b: number): number");
        Console.WriteLine("\treturn a + b");
        Console.WriteLine("end");
        Console.WriteLine("");
        Console.WriteLine("function Calculator.Reset(self: Calculator): ()");
        Console.WriteLine("\tself._result = 0");
        Console.WriteLine("end");
        Console.WriteLine("");
        Console.WriteLine("function Calculator.Max(a: number, b: number): number");
        Console.WriteLine("\tif a > b then");
        Console.WriteLine("\t\treturn a");
        Console.WriteLine("\tend");
        Console.WriteLine("\treturn b");
        Console.WriteLine("end");
        Console.WriteLine("");
        Console.WriteLine("local Operation = table.freeze({");
        Console.WriteLine("\tAdd = \"Add\",");
        Console.WriteLine("\tSubtract = 1,");
        Console.WriteLine("\tMultiply = \"Multiply\",");
        Console.WriteLine("})");
        Console.WriteLine("export type Operation = number");
        Console.WriteLine("");
        Console.WriteLine("");
        Console.WriteLine("return { Calculator = Calculator, Operation = Operation }");
    }
}
