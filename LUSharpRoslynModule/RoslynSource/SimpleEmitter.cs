    /// <summary>
    /// Returns true if the given identifier name is a known variable (parameter or local),
    /// meaning method calls on it should use colon syntax.
    /// </summary>
    /// <summary>
    /// Returns true if the given name is a known instance method of the current class.
    /// </summary>
    private bool IsInstanceMethod(string name)
    {
        for (int i = 0; i < _methodCount; i++)
        {
            if (_methodNames[i] == name && !_isMethodStatic[i]) return true;
        }
        return false;
    }

    /// <summary>
    /// Returns true if the given name is a known static method of the current class.
    /// </summary>
    private bool IsStaticMethod(string name)
    {
        for (int i = 0; i < _methodCount; i++)
        {
            if (_methodNames[i] == name && _isMethodStatic[i]) return true;
        }
        return false;
    }

    private bool IsKnownVariable(string name)
    {
        for (int i = 0; i < _paramCount; i++)
        {
            if (_paramNames[i] == name) return true;
        }
        for (int i = 0; i < _localCount; i++)
        {
            if (_localNames[i] == name) return true;
        }
        return false;
    }

    /// <summary>
    /// Returns true if the expression on the left of a member-access call is an instance
    /// (should use colon syntax) rather than a type name (should use dot syntax).
    /// Heuristic: self, self.field, known param/local, element access → instance.
    /// Single uppercase identifier not in params/locals → type name → dot.
    /// </summary>
    private bool IsInstanceCallTarget(ExpressionSyntax target, string emittedObj)
    {
        // self → always instance
        if (emittedObj == "self") return true;

        // self.field → instance
        if (emittedObj.Length > 5 && emittedObj.Substring(0, 5) == "self.") return true;

        // Element access result (e.g. self.Items[i + 1]) → instance
        if (target.Kind == 8690) return true; // ElementAccess

        // Known parameter or local variable → instance
        if (target.Kind == 8616) // IdentifierName
        {
            IdentifierNameSyntax id = (IdentifierNameSyntax)target;
            if (IsKnownVariable(id.Identifier.Text)) return true;
        }

        // Member access on a known variable (e.g. walker.Something) → instance
        if (target.Kind == 8689) // MemberAccess
        {
            MemberAccessExpressionSyntax inner = (MemberAccessExpressionSyntax)target;
            if (inner.Expression.Kind == 8616) // left side is identifier
            {
                IdentifierNameSyntax leftId = (IdentifierNameSyntax)inner.Expression;
                if (IsKnownVariable(leftId.Identifier.Text)) return true;
            }
            // Recursive: if left side is self → instance
            if (inner.Expression.Kind == 8616)
            {
                IdentifierNameSyntax leftId2 = (IdentifierNameSyntax)inner.Expression;
                if (leftId2.Identifier.Text == "self" || leftId2.Identifier.Text == "this") return true;
            }
        }

        // Default: single uppercase identifier → likely type name → dot
        return false;
    }

    // ── Invocation ────────────────────────────────────────────
    private string EmitInvocation(ExpressionSyntax expr)
    {
        InvocationExpressionSyntax inv = (InvocationExpressionSyntax)expr;

        // Build argument list
        string args = "";
        for (int i = 0; i < inv.Arguments.Length; i++)
        {
            if (i > 0) args = args + ", ";
            args = args + EmitExpression(inv.Arguments[i]);
        }

        // Member-access call: obj.Method(args)
        if (inv.Expression.Kind == 8689) // MemberAccess
        {
            MemberAccessExpressionSyntax ma = (MemberAccessExpressionSyntax)inv.Expression;
            string methodName = ma.Name.Text;
            string obj = EmitExpression(ma.Expression);

            // Console.WriteLine / Console.Write → print(args)
            if (obj == "Console" && (methodName == "WriteLine" || methodName == "Write"))
            {
                return "print(" + args + ")";
            }

            // Math special cases (methods that need argument transformation)
            if (obj == "Math")
            {
                if (methodName == "Log10" && inv.Arguments.Length == 1)
                {
                    return "math.log(" + args + ", 10)";
                }
                if (methodName == "Log2" && inv.Arguments.Length == 1)
                {
                    return "math.log(" + args + ", 2)";
                }
                if (methodName == "Log" && inv.Arguments.Length == 2)
                {
                    string logX = EmitExpression(inv.Arguments[0]);
                    string logBase = EmitExpression(inv.Arguments[1]);
                    return "math.log(" + logX + ", " + logBase + ")";
                }
                if (methodName == "Round" && inv.Arguments.Length == 2)
                {
                    string roundX = EmitExpression(inv.Arguments[0]);
                    string roundDigits = EmitExpression(inv.Arguments[1]);
                    return "math.floor(" + roundX + " * 10 ^ " + roundDigits + " + 0.5) / 10 ^ " + roundDigits;
                }
                if (methodName == "DivRem" && inv.Arguments.Length == 2)
                {
                    string divA = EmitExpression(inv.Arguments[0]);
                    string divB = EmitExpression(inv.Arguments[1]);
                    return "math.floor(" + divA + " / " + divB + "), " + divA + " % " + divB;
                }
                if (methodName == "BigMul" && inv.Arguments.Length == 2)
                {
                    string mulA = EmitExpression(inv.Arguments[0]);
                    string mulB = EmitExpression(inv.Arguments[1]);
                    return mulA + " * " + mulB;
                }
                if (methodName == "ScaleB" && inv.Arguments.Length == 2)
                {
                    string sbX = EmitExpression(inv.Arguments[0]);
                    string sbN = EmitExpression(inv.Arguments[1]);
                    return sbX + " * 2 ^ " + sbN;
                }
                if (methodName == "FusedMultiplyAdd" && inv.Arguments.Length == 3)
                {
                    string fmaX = EmitExpression(inv.Arguments[0]);
                    string fmaY = EmitExpression(inv.Arguments[1]);
                    string fmaZ = EmitExpression(inv.Arguments[2]);
                    return fmaX + " * " + fmaY + " + " + fmaZ;
                }
                if (methodName == "CopySign" && inv.Arguments.Length == 2)
                {
                    string csX = EmitExpression(inv.Arguments[0]);
                    string csY = EmitExpression(inv.Arguments[1]);
                    return "math.abs(" + csX + ") * math.sign(" + csY + ")";
                }
                if (methodName == "IEEERemainder" && inv.Arguments.Length == 2)
                {
                    string remX = EmitExpression(inv.Arguments[0]);
                    string remY = EmitExpression(inv.Arguments[1]);
                    return remX + " - " + remY + " * math.round(" + remX + " / " + remY + ")";
                }
                if (methodName == "MaxMagnitude" && inv.Arguments.Length == 2)
                {
                    string mmA = EmitExpression(inv.Arguments[0]);
                    string mmB = EmitExpression(inv.Arguments[1]);
                    return "(if math.abs(" + mmA + ") >= math.abs(" + mmB + ") then " + mmA + " else " + mmB + ")";
                }
                if (methodName == "MinMagnitude" && inv.Arguments.Length == 2)
                {
                    string mmA = EmitExpression(inv.Arguments[0]);
                    string mmB = EmitExpression(inv.Arguments[1]);
                    return "(if math.abs(" + mmA + ") <= math.abs(" + mmB + ") then " + mmA + " else " + mmB + ")";
                }
                if (methodName == "Cbrt" && inv.Arguments.Length == 1)
                {
                    return "math.sign(" + args + ") * math.abs(" + args + ") ^ (1/3)";
                }
                if (methodName == "Acosh" && inv.Arguments.Length == 1)
                {
                    return "math.log(" + args + " + math.sqrt(" + args + " * " + args + " - 1))";
                }
                if (methodName == "Asinh" && inv.Arguments.Length == 1)
                {
                    return "math.log(" + args + " + math.sqrt(" + args + " * " + args + " + 1))";
                }
                if (methodName == "Atanh" && inv.Arguments.Length == 1)
                {
                    return "0.5 * math.log((1 + " + args + ") / (1 - " + args + "))";
                }
            }

            // double/float static methods
            if (obj == "double" || obj == "Double" || obj == "float" || obj == "Single")
            {
                if (methodName == "IsNaN")
                {
                    return args + " ~= " + args;
                }
                if (methodName == "IsInfinity")
                {
                    return "(" + args + " == math.huge or " + args + " == -math.huge)";
                }
                if (methodName == "IsPositiveInfinity")
                {
                    return args + " == math.huge";
                }
                if (methodName == "IsNegativeInfinity")
                {
                    return args + " == -math.huge";
                }
                if (methodName == "IsFinite")
                {
                    return "(" + args + " == " + args + " and " + args + " ~= math.huge and " + args + " ~= -math.huge)";
                }
                if (methodName == "Parse")
                {
                    return "tonumber(" + args + ")";
                }
                if (methodName == "TryParse")
                {
                    _needsRuntime = true;
                    return "__rt.tryParse(" + args + ")";
                }
            }

            // char static methods
            if (obj == "char" || obj == "Char")
            {
                if (methodName == "IsLetter") return "string.match(string.char(" + args + "), \"%a\") ~= nil";
                if (methodName == "IsDigit") return "string.match(string.char(" + args + "), \"%d\") ~= nil";
                if (methodName == "IsLetterOrDigit") return "string.match(string.char(" + args + "), \"%w\") ~= nil";
                if (methodName == "IsWhiteSpace") return "string.match(string.char(" + args + "), \"%s\") ~= nil";
                if (methodName == "IsUpper") return "string.match(string.char(" + args + "), \"%u\") ~= nil";
                if (methodName == "IsLower") return "string.match(string.char(" + args + "), \"%l\") ~= nil";
                if (methodName == "IsPunctuation") return "string.match(string.char(" + args + "), \"%p\") ~= nil";
                if (methodName == "ToLower") return "string.byte(string.lower(string.char(" + args + ")))";
                if (methodName == "ToUpper") return "string.byte(string.upper(string.char(" + args + ")))";
                if (methodName == "GetNumericValue") return "tonumber(string.char(" + args + "))";
            }

            // Array static methods
            if (obj == "Array")
            {
                if (methodName == "Sort")
                {
                    if (inv.Arguments.Length == 1) return "table.sort(" + args + ")";
                    if (inv.Arguments.Length == 2)
                    {
                        string sortArr = EmitExpression(inv.Arguments[0]);
                        string sortCmp = EmitExpression(inv.Arguments[1]);
                        return "table.sort(" + sortArr + ", " + sortCmp + ")";
                    }
                }
                if (methodName == "Reverse")
                {
                    _needsRuntime = true;
                    return "__rt.reverseInPlace(" + args + ")";
                }
                if (methodName == "Copy")
                {
                    if (inv.Arguments.Length == 3)
                    {
                        string copySrc = EmitExpression(inv.Arguments[0]);
                        string copyDst = EmitExpression(inv.Arguments[1]);
                        string copyLen = EmitExpression(inv.Arguments[2]);
                        return "table.move(" + copySrc + ", 1, " + copyLen + ", 1, " + copyDst + ")";
                    }
                    if (inv.Arguments.Length == 5)
                    {
                        string copySrc = EmitExpression(inv.Arguments[0]);
                        string copySi = EmitExpression(inv.Arguments[1]);
                        string copyDst = EmitExpression(inv.Arguments[2]);
                        string copyDi = EmitExpression(inv.Arguments[3]);
                        string copyLen = EmitExpression(inv.Arguments[4]);
                        return "table.move(" + copySrc + ", " + copySi + " + 1, " + copySi + " + " + copyLen + ", " + copyDi + " + 1, " + copyDst + ")";
                    }
                }
                if (methodName == "IndexOf")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string idxArr = EmitExpression(inv.Arguments[0]);
                        string idxItem = EmitExpression(inv.Arguments[1]);
                        return "(table.find(" + idxArr + ", " + idxItem + ") or 0) - 1";
                    }
                }
                if (methodName == "Clear")
                {
                    _needsRuntime = true;
                    return "__rt.arrayClear(" + args + ")";
                }
                if (methodName == "Fill")
                {
                    _needsRuntime = true;
                    return "__rt.arrayFill(" + args + ")";
                }
                if (methodName == "Exists")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string exArr = EmitExpression(inv.Arguments[0]);
                        string exPred = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.any(" + exArr + ", " + exPred + ")";
                    }
                }
                if (methodName == "Find")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string fArr = EmitExpression(inv.Arguments[0]);
                        string fPred = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.firstOrDefault(" + fArr + ", " + fPred + ")";
                    }
                }
                if (methodName == "FindAll")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string fArr = EmitExpression(inv.Arguments[0]);
                        string fPred = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.where(" + fArr + ", " + fPred + ")";
                    }
                }
                if (methodName == "FindIndex")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string fArr = EmitExpression(inv.Arguments[0]);
                        string fPred = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.findIndex(" + fArr + ", " + fPred + ")";
                    }
                }
                if (methodName == "ConvertAll")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string cArr = EmitExpression(inv.Arguments[0]);
                        string cConv = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.select(" + cArr + ", " + cConv + ")";
                    }
                }
                if (methodName == "TrueForAll")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string tArr = EmitExpression(inv.Arguments[0]);
                        string tPred = EmitExpression(inv.Arguments[1]);
                        _needsRuntime = true;
                        return "__rt.all(" + tArr + ", " + tPred + ")";
                    }
                }
                if (methodName == "LastIndexOf")
                {
                    _needsRuntime = true;
                    return "__rt.lastIndexOfArray(" + args + ")";
                }
                if (methodName == "BinarySearch")
                {
                    _needsRuntime = true;
                    return "__rt.binarySearch(" + args + ")";
                }
                if (methodName == "Resize")
                {
                    _needsRuntime = true;
                    return "__rt.arrayResize(" + args + ")";
                }
            }

            // Enum static methods
            if (obj == "Enum")
            {
                if (methodName == "Parse" && inv.Arguments.Length >= 2)
                {
                    string enumType = EmitExpression(inv.Arguments[0]);
                    string enumVal = EmitExpression(inv.Arguments[1]);
                    return enumType + "[" + enumVal + "]";
                }
                if (methodName == "IsDefined" && inv.Arguments.Length >= 2)
                {
                    string enumType = EmitExpression(inv.Arguments[0]);
                    string enumVal = EmitExpression(inv.Arguments[1]);
                    return enumType + "[" + enumVal + "] ~= nil";
                }
                if (methodName == "GetValues")
                {
                    _needsRuntime = true;
                    return "__rt.values(" + args + ")";
                }
                if (methodName == "GetNames")
                {
                    _needsRuntime = true;
                    return "__rt.keys(" + args + ")";
                }
            }

            // Random methods (Roblox Random class)
            if (methodName == "Next")
            {
                string nextVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (nextVarType != null && nextVarType == "Random")
                {
                    if (inv.Arguments.Length == 0) return obj + ":NextInteger(0, 2147483647)";
                    if (inv.Arguments.Length == 1) return obj + ":NextInteger(0, " + args + " - 1)";
                    if (inv.Arguments.Length == 2)
                    {
                        string nextMin = EmitExpression(inv.Arguments[0]);
                        string nextMax = EmitExpression(inv.Arguments[1]);
                        return obj + ":NextInteger(" + nextMin + ", " + nextMax + " - 1)";
                    }
                }
            }
            if (methodName == "NextDouble")
            {
                string nextVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (nextVarType != null && nextVarType == "Random") return obj + ":NextNumber()";
            }
            if (methodName == "NextSingle")
            {
                string nextVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (nextVarType != null && nextVarType == "Random") return obj + ":NextNumber()";
            }

            // Stopwatch methods
            if (obj == "Stopwatch")
            {
                if (methodName == "StartNew")
                {
                    _needsRuntime = true;
                    return "__rt.Stopwatch.StartNew()";
                }
                if (methodName == "GetTimestamp") return "os.clock()";
            }
            if (methodName == "Start" || methodName == "Stop" || methodName == "Reset" || methodName == "Restart")
            {
                string swVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (swVarType != null && swVarType == "Stopwatch")
                {
                    _needsRuntime = true;
                    return "__rt.Stopwatch." + methodName + "(" + obj + ")";
                }
            }

            // Guid.NewGuid()
            if (obj == "Guid" && methodName == "NewGuid")
            {
                _needsRuntime = true;
                return "__rt.newGuid()";
            }

            // Thread.Sleep → task.wait
            if (obj == "Thread" && methodName == "Sleep")
            {
                return "task.wait(" + args + " / 1000)";
            }

            // TimeSpan static factory methods → seconds as number
            if (obj == "TimeSpan")
            {
                if (methodName == "FromSeconds") return args;
                if (methodName == "FromMilliseconds") return "(" + args + " / 1000)";
                if (methodName == "FromMinutes") return "(" + args + " * 60)";
                if (methodName == "FromHours") return "(" + args + " * 3600)";
                if (methodName == "FromDays") return "(" + args + " * 86400)";
            }

            // DateTime methods
            if (obj == "DateTime")
            {
                if (methodName == "Parse") return "tonumber(" + args + ") or 0";
            }

            // Nullable.GetValueOrDefault
            if (methodName == "GetValueOrDefault")
            {
                if (inv.Arguments.Length == 0)
                {
                    return "if " + obj + " ~= nil then " + obj + " else 0";
                }
                return "if " + obj + " ~= nil then " + obj + " else " + args;
            }

            // bit32 / BitConverter basics
            if (obj == "BitConverter")
            {
                if (methodName == "GetBytes") return "{ " + args + " }";
                if (methodName == "ToInt32") return "bit32.bor(0, " + args + ")";
                if (methodName == "ToUInt32") return "bit32.bor(0, " + args + ")";
            }

            // string.IsNullOrEmpty(s) → (s == nil or #s == 0)
            if (obj == "string" && methodName == "IsNullOrEmpty")
            {
                return "(" + args + " == nil or #" + args + " == 0)";
            }

            // StringBuilder methods (we model StringBuilder as a plain string)
            if (methodName == "Append")
            {
                return obj + " = " + obj + " .. " + args;
            }
            if (methodName == "AppendLine")
            {
                if (args.Length > 0)
                {
                    return obj + " = " + obj + " .. " + args + " .. \"\\n\"";
                }
                return obj + " = " + obj + " .. \"\\n\"";
            }
            if (methodName == "ToString" && inv.Arguments.Length == 0)
            {
                return "tostring(" + obj + ")";
            }

            // List / array methods
            if (methodName == "Add")
            {
                // HashSet.Add(x) → set[x] = true
                string addVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (addVarType != null && addVarType.Length > 8 && addVarType.Substring(0, 8) == "HashSet<")
                {
                    return obj + "[" + args + "] = true";
                }
                // Dictionary.Add(key, value) → dict[key] = value
                if (addVarType != null && addVarType.Length > 11 && addVarType.Substring(0, 11) == "Dictionary<")
                {
                    if (inv.Arguments.Length == 2)
                    {
                        string dictKey = EmitExpression(inv.Arguments[0]);
                        string dictVal = EmitExpression(inv.Arguments[1]);
                        return obj + "[" + dictKey + "] = " + dictVal;
                    }
                }
                return "table.insert(" + obj + ", " + args + ")";
            }
            if (methodName == "AddRange")
            {
                return "table.move(" + args + ", 1, #" + args + ", #" + obj + " + 1, " + obj + ")";
            }
            if (methodName == "Clear")
            {
                // StringBuilder.Clear() → sb = ""
                string clearVarType = GetVariableType(GetIdentifierName(ma.Expression));
                if (clearVarType != null && clearVarType == "StringBuilder")
                {
                    return obj + " = \"\"";
                }
                return "table.clear(" + obj + ")";
            }
